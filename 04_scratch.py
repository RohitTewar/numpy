import numpy as np
# Create array from zeros
zeros = np.zeros((2, 3))
print("Array of zeros:\n", zeros)

# Create array from ones
ones = np.ones((2, 3))
print("Array of ones:\n", ones)

# Create array from full
full =np.full((2, 3), 7)
print("Array of sevens:\n", full)

# Create array from random numbers
random = np.random.random((2, 3))
print("Array of random numbers:\n", random)

# Create array from arange
sequence = np.arange(0, 10, 2)
print("Array of sequence:\n", sequence)

